export type PaletteStep = {
  step: string;
  hex: string;
  cssVar: string;
};

export type Palette = {
  id: string;
  name: string;
  steps: PaletteStep[];
};

const build = (id: string, steps: Array<[string, string]>): Palette => ({
  id,
  name: id.charAt(0).toUpperCase() + id.slice(1),
  steps: steps.map(([step, hex]) => ({
    step,
    hex,
    cssVar: `--seed-color-${id}-${step}`,
  })),
});

