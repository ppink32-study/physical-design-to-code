#!/usr/bin/env node
/* eslint-disable */
/**
 * Walk public/icon and emit a TS module listing every SVG with public URL,
 * group (sub-folder), and a normalized search-friendly name.
 *
 * Re-run when icons are added/renamed:
 *   npm run gen:icons
 */

const fs = require("fs");
const path = require("path");

const ROOT = path.resolve(__dirname, "..");
const ICON_DIR = path.join(ROOT, "public/icon");
const OUT = path.join(ROOT, "src/commons/constants/foundation/icons.generated.ts");

function walk(dir, baseSegments) {
  const out = [];
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  for (const entry of entries) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      out.push(...walk(full, baseSegments.concat(entry.name)));
    } else if (entry.isFile() && entry.name.toLowerCase().endsWith(".svg")) {
      const segments = baseSegments.concat(entry.name);
      const baseName = entry.name.replace(/\.svg$/i, "");
      const group = baseSegments.length > 0 ? baseSegments.join(" / ") : "Default";
      const url = "/icon/" + segments.map(encodeURIComponent).join("/");
      const keywords = [baseName].concat(baseSegments).join(" ").toLowerCase()
        .replace(/[^a-z0-9가-힣]+/g, " ").trim();
      out.push({ name: baseName, group, url, keywords });
    }
  }
  return out;
}

function main() {
  if (!fs.existsSync(ICON_DIR)) {
    console.error("public/icon not found:", ICON_DIR);
    process.exit(1);
  }
  const list = walk(ICON_DIR, []).sort((a, b) => {
    if (a.group !== b.group) return a.group.localeCompare(b.group);
    return a.name.localeCompare(b.name, undefined, { numeric: true, sensitivity: "base" });
  });
  const groups = Array.from(new Set(list.map((i) => i.group))).sort();

  const lines = [];
  lines.push("// Auto-generated by scripts/generate-icon-catalog.js \u2014 do not edit directly.");
  lines.push("// Run: npm run gen:icons");
  lines.push("");
  lines.push("export type IconEntry = {");
  lines.push("  /** File base name without extension */");
  lines.push("  name: string;");
  lines.push("  /** Folder grouping (\"Default\" for root level) */");
  lines.push("  group: string;");
  lines.push("  /** Public URL beneath /public */");
  lines.push("  url: string;");
  lines.push("  /** Lowercase searchable string */");
  lines.push("  keywords: string;");
  lines.push("};");
  lines.push("");
  lines.push("export const ICON_ENTRIES: readonly IconEntry[] = " + JSON.stringify(list, null, 2) + " as const;");
  lines.push("");
  lines.push("export const ICON_GROUPS: readonly string[] = " + JSON.stringify(groups, null, 2) + " as const;");
  lines.push("");

  fs.mkdirSync(path.dirname(OUT), { recursive: true });
  fs.writeFileSync(OUT, lines.join("\n"), "utf8");
  console.log("Wrote " + list.length + " icons \u2192 " + path.relative(ROOT, OUT));
}

main();
